    public void getAllPatients() {
        try {
            ResultSet rs = GlobalVariables.patientDao.all();
            patientC.removeAllItems();
            patientC.insertItemAt(new PatientCombo(-1, ""), 0);
            while (rs.next()) {
                String fname = rs.getString("fname");
                String lname = rs.getString("lname");
                String name = lname + " " + fname;
                int idPatient = rs.getInt("id");
                PatientCombo pc = new PatientCombo(idPatient, name);
                patientC.addItem(pc);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void getAllRdv() {
        try {
            DefaultTableModel tbModel = (DefaultTableModel) jTable1.getModel();
            tbModel.setRowCount(0);
            ResultSet rs = GlobalVariables.rdvDao.all();
            while (rs.next()) {
                String fname = rs.getString("fname");
                String lname = rs.getString("lname");
                String name = lname + " " + fname;
                Date date = rs.getDate("date");
                String time = rs.getString("temps");
                String dateTime = date + " AT " + time;
                Object tbData[] = {name, dateTime};

                tbModel.addRow(tbData);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

