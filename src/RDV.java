    public void validateData() {
        if (this.idPatient == -1) {
            PatientCombo itemSelected = (PatientCombo) patientC.getSelectedItem();
            int idPatient = itemSelected.getIdPatient();
            this.idPatient = idPatient;

        }
        String time = tempRdv.getText();
        if (!time.equals("") && idPatient != -1) {
            addRdv.setEnabled(true);
        } else {
            addRdv.setEnabled(false);
        }
    }

    public void clearData() {
        tempRdv.setText("");
        patientC.setSelectedIndex(0);

    }

